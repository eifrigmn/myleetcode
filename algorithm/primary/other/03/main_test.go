package _03

import (
	"testing"

	. "github.com/smartystreets/goconvey/convey"
)

func TestReverseBits(t *testing.T) {
	Convey("颠倒二级制位", t, func() {
		// 输入: 43261596(00000010100101000001111010011100)
		// 输出: 964176192(00111001011110000010100101000000)
		So(reverseBits(43261596), ShouldEqual, 964176192)
		// 输入：4294967293(11111111111111111111111111111101)
		// 输出：3221225471(10111111111111111111111111111111)
		So(reverseBits(4294967293), ShouldEqual, 3221225471)
		// 输入：43261596(00000010100101000001111010011100)
		// 输出：964176192 (00111001011110000010100101000000)
		So(reverseBits(43261596), ShouldEqual, 964176192)
	})
}
